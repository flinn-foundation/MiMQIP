package flinn.rcopia.service;

public class RcopiaTransformationException extends Exception
{

	private static final long serialVersionUID = 1L;

	public RcopiaTransformationException()
	{
		super();
	}

	public RcopiaTransformationException(String message, Throwable cause)
	{
		super(message, cause);
	}

	public RcopiaTransformationException(String message)
	{
		super(message);
	}

	public RcopiaTransformationException(Throwable cause)
	{
		super(cause);
	}
}
